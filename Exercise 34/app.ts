// Animals

const animals: string[] = ["dog", "cat", "rabbit"];

// Path: app.ts

for (const animal of animals) {
  if (animal === "dog") {
    console.log(`A ${animal} has four legs`);
  } else if (animal == "cat") {
    console.log(`A ${animal} has wonderful whiskers`);
  } else if (animal == "rabbit") {
    console.log(`A ${animal} has a fluffy tail`);
  }
}

